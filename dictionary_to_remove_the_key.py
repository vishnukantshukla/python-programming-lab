# Delete the entry of the dictionary using del keyword

dct={'Name':"Vishnukant shukla",'Roll number ':56,"College":"Gla university"}
del dct['Name']
print(dct)

#   Delete the entry of the dictionary using pop method 
dct={'Name':"Vishnukant shukla",'Roll number ':56,"College":"Gla university"}
dct.pop('Name')
print(dct)

